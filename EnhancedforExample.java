public class EnhancedforExample {
    public static void main(String[] args) {
        for(int i=0; i<10; i++){
            System.out.println(i);
        }
        System.out.println();
        int [] k = {0, 1, 2, 3, 4, 5};
        for(int index : k) {
            System.out.println(index);
        }
    }
}
